public class Variables {
    public static void main(String[] args) {
           // int String boolean float double
        int contador = 0;
        for(int i=0; i<10;i++){
            contador=i;
            System.out.println(contador);
        }
        contador = 0;
        System.out.println("-----------");
        while (contador<10){
           contador++;
            System.out.println(contador);
        }
    }
}
